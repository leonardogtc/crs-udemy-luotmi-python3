# A função print()
# Nome da função e argumentos entre parênteses
# função com separador (sep="") e quebrando linha (end='\n')
# O python é case sensitive

print(12, 34, sep="-", end='##\n')
'''
OBS: No VSC, na barra de status, existe a informação LF ou CRLF.
A CRLF é uma quebra de linha específica do Windows e seus caracteres
de quebra são: \r (return) \n (line feet)
Windows -> \r\n
'''
print(56, 78, sep='-')
