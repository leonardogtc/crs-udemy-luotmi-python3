# Concatenação (+) e repetição (*) com operadores aritméticos
concatenacao = 'Luiz' + ' ' + 'Otávio'
print(concatenacao)

# Uma string multiplicada por um valor
# causará a repetição dessa string o número
# de vezes que foi multiplicada.

# Aqui resultará na repetição de A
a_dez_vezes = 'A' * 10

# Aqui resultará na repetição do nome
tres_vezes_luiz = 3 * 'Luiz'

# Imprime a saída
print(a_dez_vezes)
print(tres_vezes_luiz)
