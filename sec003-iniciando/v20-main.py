# Tipo int e float
# int -> número inteiro
# O tipo int representa qualquer número positivo ou negativo
# int sem sinal é considerado positivo

print(11)
print(-11)
print(0)  # Nesse contexto o zero é considerado número inteiro

# float -> número com ponto flutuante
# o tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.

print(1.1, 10.11)
print(0.0, -1.5)

# A função type mostra o tipo que o Python inferiu no valor
print(type(11))
print(type(0))
print(type('Leonardo'))
