'''
Python = Linguagem de Programação
Tipagem = Dinâmica + Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
'''

print(1234)

# Aspas simples
print('Leonardo Tavares')
# Aspas duplas
print("Leonardo Tavares")
# Escape
print("Leonardo Tavares \"Programador\"")
# r -> Mostra o caractere de escape
print(r"Leonardo Tavares \"Programador\"")
# Invertendo aspas para deixar o código mais limpo
print("Leonardo Tavares 'Programador'")
